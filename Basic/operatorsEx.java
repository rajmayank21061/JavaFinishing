public class operatorsEx {
    public static void main(String[] args) {
        int a = 9;//1001
        int b = 11;//1011
        // Bit wise operator
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(a << 1);
        System.out.println(1 >> b);
    }
}
